package Weapons;


public class Weapon {
    public int wpow;
    public int wspd;

    public Weapon(int wpow , int wspd){
        
        this.wpow = wpow;

        this.wspd = wspd;

    }

    public int getWpow() {
        return wpow;
    }

    public int getWspd() {
        return wspd;
    }

}
